  async function selectTemplate(templateId) {
    const templateCard = document.querySelector(`[data-template="${templateId}"]`);
    if (templateCard) {
      templateCard.click();
      await sleep(500); // 等待选择生效
      
      // 验证是否选中
      if (templateCard.classList.contains('selected')) {
        log(`模板已选择: ${templateId}`, 'success');
        return true;
      } else {
        log(`模板选择失败: ${templateId}`, 'error');
        return false;
      }
    } else {
      log(`未找到模板卡片: ${templateId}`, 'error');
      return false;
    }
  }
  
  async function clickGenerateButton() {
    const generateBtn = document.getElementById('generateBtn');
    if (generateBtn) {
      if (generateBtn.disabled) {
        log('生成按钮被禁用', 'warning');
        return false;
      }
      
      generateBtn.click();
      log('已点击生成按钮');
      return true;
    } else {
      log('未找到生成按钮', 'error');
      return false;
    }
  }
  
  async function waitForResult(maxWaitTime = 10000) {
    const startTime = Date.now();
    
    while (Date.now() - startTime < maxWaitTime) {
      const resultDiv = document.getElementById('result');
      if (resultDiv && resultDiv.style.display !== 'none' && resultDiv.innerHTML.trim()) {
        log('生成结果已显示', 'success');
        return true;
      }
      await sleep(500);
    }
    
    log('等待结果超时', 'error');
    return false;
  }
  
  async function testTemplate(template) {
    log(`\n🧪 开始测试模板: ${template.name} (${template.id})`);
    
    try {
      // 1. 选择模板
      const templateSelected = await selectTemplate(template.id);
      if (!templateSelected) {
        return { success: false, error: '模板选择失败' };
      }
      
      await sleep(1000);
      
      // 2. 点击生成按钮
      const buttonClicked = await clickGenerateButton();
      if (!buttonClicked) {
        return { success: false, error: '生成按钮点击失败' };
      }
      
      // 3. 等待结果
      const resultShown = await waitForResult();
      if (!resultShown) {
        return { success: false, error: '生成结果未显示' };
      }
      
      // 4. 检查结果内容
      const resultDiv = document.getElementById('result');
      const resultContent = resultDiv.innerHTML;
      
      if (resultContent.includes('生成成功') || resultContent.includes('查看结果')) {
        log(`✅ ${template.name} 测试成功`, 'success');
        return { success: true, result: resultContent };
      } else if (resultContent.includes('错误') || resultContent.includes('失败')) {
        log(`❌ ${template.name} 测试失败: 生成出错`, 'error');
        return { success: false, error: '生成出错', result: resultContent };
      } else {
        log(`⚠️ ${template.name} 测试结果不明确`, 'warning');
        return { success: false, error: '结果不明确', result: resultContent };
      }
      
    } catch (error) {
      log(`❌ ${template.name} 测试异常: ${error.message}`, 'error');
      return { success: false, error: error.message };
    }
  }
  
